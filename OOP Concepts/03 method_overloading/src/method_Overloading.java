public class method_Overloading {

    int sum(int a,int b)
    {
        return a+b;
    }
    double sum(double a,double b)
    {
        return a+b;
    }
    int sum(int a,int b,int c)
    {
        return a+b+c;
    }
    void sum()
    {
        System.out.println("Anything");
    }
    
}
