public class type_Casting {
    public static void main(String[] args) {
        //implisive datatype:
        int x=57;
        double y=x;
        System.out.println(y);
        //explicit datatype
        double d=10.4;
        int i= (int)d;
        System.out.println(i);
        char z=(char)x;
        System.out.println(z);
    }
}
/*
by typecasting we can change datatype of a variable.
there are two type of typecasting inplisive typecasting and explicit datatype

 */